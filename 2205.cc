string bitrep(int num, int n) {
    string res;
    for (int i = n - 1; i >= 0; --i) {
        res.append(1, (num & (1 << i)) ? '1' : '0');
    }
    return res;
}

int main() {
    #ifndef LOCAL
      nfs();
    #endif

    int n;
    cin >> n;

    for (int i = 0; i <= (1 << n); ++i) {
        cout << bitrep(i ^ (i >> 1), n) << '\n';
    }
}

